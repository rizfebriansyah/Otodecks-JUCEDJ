void MainComponent::resized()
{
    // This is called when the MainContentComponent is resized.
    // If you add any child components, this is where you should
    // update their positions.
    
    int lines = 80;
    int playerRight = getWidth() * 22 / lines;
    playlistComponent.setBounds(0, 0, playerRight, getHeight());
    deckGUI1.setBounds(playerRight, 0, getWidth() - playerRight, getHeight() / 2);
    deckGUI2.setBounds(playerRight, getHeight() / 2, getWidth() - playerRight, getHeight() / 2);
}





